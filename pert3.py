# BELAJAR TIPE DATA

# TIPE DATA INTIGER : BILANGAN BULAT
bil_integer = 100
print (type(bil_integer))

print ("tipe data dari bil_integer adalah :",(type(bil_integer)))

# tipe data float : bilangan desimal
bil_float = 2.5
print (type(bil_float))
print ("tipe data dari bil_float adalah :",(type(bil_float)))

# data string = kumpulan karakter
bil_string = "30000.s"
print (type(bil_string))
"tipe data dari bil_string adalah :", (type(bil_string))
# tipe data boolean : 0/1, ture/false, ya/tidak
bil_bol = False
print (type(bil_bol))
print ("tipe data bil_bol adalah :", (type(bil_bol)))
from ctypes import c_double
bil_double = c_double(30000000)
print (type(bil_double))
